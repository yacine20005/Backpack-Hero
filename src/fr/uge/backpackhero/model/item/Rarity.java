package fr.uge.backpackhero.model.item;

/**
 * Enum representing the rarity levels of items in the game.
 * 
 * @author Yacine
 */
public enum Rarity {
    /** Common items, easily found. */
    COMMON,
    /** Uncommon items, slightly harder to find. */
    UNCOMMON,
    /** Rare items, valuable and powerful. */
    RARE,
    /** Epic items, very powerful and expensive. */
    EPIC,
    /** Exotic items, unique and extremely powerful. */
    EXOTIC
}
